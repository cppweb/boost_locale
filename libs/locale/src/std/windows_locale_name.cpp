std::string get_windows_language_by_code(std::string const &key)
{
    return find_value(key,languages,sizeof(languages)/sizeof(languages[0]));
}

std::string get_windows_country_by_code(std::string const &key)
{
    return find_value(key,countries,sizeof(countries)/sizeof(countries[0]));
}



