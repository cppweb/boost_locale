    std::string find_value(std::string const &skey,key_value const *begin,size_t len)
    {
        key_value const *end = begin+len;
        key_value key = { skey.c_str(), 0 };
        key_value const *p = std::lower_bound(begin,end,key);
        if(p==end || strcmp(p->key,skey.c_str())!=0)
            return std::string();
        else
            return p->value;
    }

