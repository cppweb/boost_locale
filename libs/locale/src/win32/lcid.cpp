unsigned locale_to_lcid(std::string const &locale_name)
{
    if(locale_name.empty()) {
        return LOCALE_USER_DEFAULT;
    } 
    boost::locale::impl_std::locale_data d;
    d.parse(locale_name);
    if(!d.utf8)
        return 0;
    unsigned matched = 0;
    unsigned lcid = 0;
    for(size_t i=0;i<sizeof(all_locales)/sizeof(all_locales[0]);i++) {
        unsigned cmatched = 0;
        lcid_data const & cur = all_locales[i];
        if(d.language == cur.language)
            cmatched++;
        if(cmatched == 0)
            continue;
        if(d.country == cur.country)
            cmatched+=2;
        if(d.variant == cur.variant)
            cmatched+=4;
        if(cmatched == 7)
            return cur.lcid;
        if(cmatched > matched) {
            lcid = cur.lcid;
            matched = cmatched;
        }
    }
    return lcid;
}

